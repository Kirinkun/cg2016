function getSizeMat(size) {
  return mat4.multiply(mat4.create(), mat4.create(), glm.scale(size,size,size));
}

class UfoSGNode extends SGNode {

  constructor(size,children) {
    super(children)
    this.root = new TransformationExtendedNode(getSizeMat(size), new RenderSGNode(ufoModel));
  }

  scale(size) {
    this.root.setMatrix(getSizeMat(size));
  }

  render(context) {
    this.root.render(context);
  }
}
